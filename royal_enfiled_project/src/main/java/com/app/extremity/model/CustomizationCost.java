package com.app.extremity.model;

public class CustomizationCost {

	 private String customizationCostId;
	 private String part;
	 private long cost;
	public String getCustomizationCostId() {
		return customizationCostId;
	}
	public void setCustomizationCostId(String customizationCostId) {
		this.customizationCostId = customizationCostId;
	}
	public String getPart() {
		return part;
	}
	public void setPart(String part) {
		this.part = part;
	}
	public long getCost() {
		return cost;
	}
	public void setCost(long cost) {
		this.cost = cost;
	}
	 
	 
	 
}
